#include <iostream>

using namespace std;

int main()
{
    int a, b, c;
    cout << "Masukan angka pertama: ";
    cin >> a;
    cout << "Masukan angka kedua: ";
    cin >> b;
    c = a + b;
    cout << "Hasil dari ";
    cout << a;
    cout << " + ";
    cout << b;
    cout << " = " << c;
    cout << endl;
    cout << "Selesai" << endl;
    return 0;
}
